# Definition for a binary tree node.
class TreeNode(object):
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None


class Solution(object):
    def postorderTraversal(self, root):
        """
        :type root: TreeNode
        :rtype: List[int]
        """
        ans, stack, cur = [], [], root
        while len(stack) or cur:
            if cur:
                stack.append(cur)
                ans.append(cur.val)
                cur = cur.right
            else:
                cur = stack.pop()
                cur = cur.left
        return ans[::-1]
