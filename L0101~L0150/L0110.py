# Definition for a binary tree node.
class TreeNode(object):
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None


class Solution(object):
    def isBalanced(self, root):
        """
        :type root: TreeNode
        :rtype: bool
        """

        def ib(node):
            if node is None:
                return 0
            l_depth = ib(node.left)
            r_depth = ib(node.right)
            assert abs(l_depth - r_depth) <= 1
            return max(l_depth, r_depth) + 1

        try:
            ib(root)
        except:
            return False
        else:
            return True


