class Solution(object):
    def addDigits(self, num):
        """
        :type num: int
        :rtype: int
        """
        res = str(num)
        while len(res) != 1:
            temp = 0
            for i in range(len(res)):
                temp += int(res[i])
            res = str(temp)
        return int(res)
