class Solution(object):
    def lengthOfLIS(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        if len(nums) == 0:
            return 0
        if len(nums) == 1:
            return 1
        flag = 1
        max_flag = 1
        for i in range(1, len(nums)):
            if nums[i] > nums[i - 1]:
                flag += 1
            else:
                max_flag = max(flag, max_flag)
                flag = 1
        return max_flag
        pass


