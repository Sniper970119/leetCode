class Solution(object):
    def missingNumber(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        number1 = 0
        number2 = 0

        for i in range(len(nums)+1):
            number1 ^= i
        for j in nums:
            number2 ^= j

        return number1^number2