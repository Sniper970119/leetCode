class Solution(object):
    def hIndex(self, citations):
        """
        :type citations: List[int]
        :rtype: int
        """
        citations = sorted(citations, reverse=True)
        for i in range(len(citations)):
            if i + 1 > citations[i]:
                return i
        return len(citations)


