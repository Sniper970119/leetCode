class Solution(object):
    def romanToInt(self, s):
        """
        :type s: str
        :rtype: int
        """
        alpha_num_dict = {
            'M': 1000,
            'D': 500,
            'C': 100,
            'L': 50,
            'X': 10,
            'V': 5,
            'I': 1
        }
        sum = 0
        for i in range(len(s)):
            if i < len(s) - 1 and alpha_num_dict[s[i]] < alpha_num_dict[s[i + 1]]:
                sum -= alpha_num_dict[s[i]]
            else:
                sum += alpha_num_dict[s[i]]
        return sum


