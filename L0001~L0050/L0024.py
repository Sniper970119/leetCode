# Definition for singly-linked list.
class ListNode(object):
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution(object):
    def swapPairs(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        if head is None:
            return
        node = head.next
        if node is None:
            return head
        head.next = node.next
        node.next = head
        head.next = self.swapPairs(head.next)
        return node


