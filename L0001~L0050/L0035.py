class Solution(object):
    def searchInsert(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: int
        """
        if len(nums) == 0:
            return 0
        if nums[0] > target:
            return 0
        if nums[-1] < target:
            return len(nums)
        if nums[0] == target:
            return 0
        for i in range(len(nums) - 1):
            if nums[i] < target < nums[i + 1]:
                return i + 1
            if nums[i] == target:
                return i
            if nums[i + 1] == target:
                return i + 1


