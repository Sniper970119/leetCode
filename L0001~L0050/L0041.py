class Solution(object):
    def firstMissingPositive(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        n = len(nums)
        temp_list = [0 for _ in range(n)]
        for each in nums:
            if each <= n and each > 0:
                temp_list[each - 1] = each
        for i in range(n):
            if temp_list[i] != i + 1:
                return i + 1
        return n + 1


