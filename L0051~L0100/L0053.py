class Solution(object):
    def maxSubArray(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        if len(nums) <= 0:
            return
        if len(nums) == 1:
            return nums[0]
        dp = [0] * len(nums)
        for i in range(len(nums)):
            dp[i] = nums[i]
        for i in range(1, len(nums)):
            if dp[i] < nums[i] + dp[i - 1]:
                dp[i] = nums[i] + dp[i - 1]
        return max(dp)


