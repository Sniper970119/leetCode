# Definition for singly-linked list.
class ListNode(object):
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution(object):
    def rotateRight(self, head, k):
        """
        :type head: ListNode
        :type k: int
        :rtype: ListNode
        """
        if not head: return None
        orig_head, cnt = head, 1
        while head.next:
            head, cnt = head.next, cnt + 1
        head.next = orig_head

        step = cnt - k % cnt - 1
        while step > 0:
            orig_head, step = orig_head.next, step - 1

        new_head, orig_head.next = orig_head.next, None
        return new_head
