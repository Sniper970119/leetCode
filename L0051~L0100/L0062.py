class Solution(object):
    def uniquePaths(self, m, n):
        """
        :type m: int
        :type n: int
        :rtype: int
        """
        if m == 1 or n == 1:
            return 1
        m = m - 1
        n = n - 1
        res = 1
        for i in range(m + n, m, -1):
            res *= i
        for i in range(1, n + 1):
            res /= i

        return int(res)


