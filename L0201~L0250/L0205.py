class Solution(object):
    def isIsomorphic(self, s, t):
        """
        :type s: str
        :type t: str
        :rtype: bool
        """
        for i in range(len(s)):
            if s[i + 1:].find(s[i]) != t[i + 1:].find(t[i]):
                return False
        return True


