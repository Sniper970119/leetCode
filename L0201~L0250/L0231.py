import math

class Solution(object):
    def isPowerOfTwo(self, n):
        """
        :type n: int
        :rtype: bool
        """
        if n == 1:
            return True
        if n == 0:
            return False
        while n > 2:
            # n = math.sqrt(n)
            n = n / 2
        if n == 2:
            return True
        else:
            return False


