class Solution(object):
    def combinationSum3(self, k, n):
        """
        :type k: int
        :type n: int
        :rtype: List[List[int]]
        """
        res_list = []

        def back(idx, temp_list):
            if len(temp_list) == k:
                if sum(temp_list) == n:
                    res_list.append(temp_list)
                return
            for i in range(idx + 1, 10):
                temp_list.append(i)
                back(i, temp_list[:])
                temp_list = temp_list[:-1]

        back(0, [])
        return res_list


