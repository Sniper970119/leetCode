class Solution(object):
    def containsNearbyAlmostDuplicate(self, nums, k, t):
        """
        :type nums: List[int]
        :type k: int
        :type t: int
        :rtype: bool
        """
        for i in range(len(nums)):
            for j in range(i + 1, k + i + 1):
                if j < len(nums) and abs(nums[i] - nums[j]) <= t:
                    return True
        return False
        pass


