class Solution(object):
    def searchMatrix(self, matrix, target):
        """
        :type matrix: List[List[int]]
        :type target: int
        :rtype: bool
        """
        if len(matrix) == 0:
            return False
        if len(matrix[0]) == 0:
            return False
        for i in range(len(matrix)):
            if matrix[i][0] > target:
                break
            for j in range(len(matrix[0])):
                if matrix[i][j] == target:
                    return True
                if matrix[i][j] > target:
                    break
        return False
        pass


