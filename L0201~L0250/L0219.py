class Solution(object):
    def containsNearbyDuplicate(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: bool
        """
        for i in range(len(nums)):
            for j in range(i + 1, k + i + 1):
                if j < len(nums) and nums[i] == nums[j]:
                    return True
        return False


