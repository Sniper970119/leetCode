# Definition for singly-linked list.
class ListNode(object):
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution(object):
    def reverseList(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        h = ListNode(-1)
        if head is None:
            return None
        h.next = ListNode(head.val)
        t = head.next
        while t != None:
            temp = ListNode(t.val)
            temp.next = h.next
            h.next = temp
            t = t.next
        return h.next


